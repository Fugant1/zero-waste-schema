-- BLOCO DE TRANSPORTADOR
-- NESSE BLOCO PODEMOS OBSERVAR TABELAS RELACIONADAS AOS USUÁRIOS COM A FUNÇÃO DE TRANSPORTADOR
CREATE TABLE TRANSPORTADOR (
    USUARIO_TRA VARCHAR2(14) NOT NULL,

    CONSTRAINT PK_USUARIO_TRANSPORTADOR PRIMARY KEY (USUARIO_TRA), 
    CONSTRAINT FK_USUARIO_TRANSPORTADOR FOREIGN KEY (USUARIO_TRA) REFERENCES USUARIO (CPF_CNPJ)
);


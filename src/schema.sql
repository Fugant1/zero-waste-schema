-- USUÁRIO BASE
CREATE TABLE USUARIO (
    CPF_CNPJ VARCHAR2(14) NOT NULL, --CPF com traço e pontos ou CNPJ só números
    NOME VARCHAR2(50) NOT NULL,
    TELEFONE VARCHAR2(20) NOT NULL,
    EMAIL VARCHAR2(50) NOT NULL,
    --TIPO VARCHAR2(13) NOT NULL, --Aparentemente n se usa

    CONSTRAINT PK_CPF_CPNJ_USUARIO PRIMARY KEY (CPF_CNPJ),
    CONSTRAINT UNIQUE_EMAIL_USUARIO UNIQUE (EMAIL),
    CONSTRAINT CHECK_EMAIL_FORMAT CHECK (REGEXP_LIKE(EMAIL, '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,10}$'))    
);


-- BLOCO DE TRANSPORTADOR
-- NESSE BLOCO PODEMOS OBSERVAR TABELAS RELACIONADAS AOS USUÁRIOS COM A FUNÇÃO DE TRANSPORTADOR
CREATE TABLE TRANSPORTADOR (
    USUARIO_TRA VARCHAR2(14) NOT NULL,
    CONSTRAINT PK_USUARIO_TRANSPORTADOR PRIMARY KEY (USUARIO_TRA), 
    CONSTRAINT FK_USUARIO_TRA FOREIGN KEY (USUARIO_TRA) REFERENCES USUARIO (CPF_CNPJ) ON DELETE CASCADE
);

CREATE TABLE REQ_ENTREGA (
    REQUISICAO_ NUMBER NOT NULL,
    DESTINO VARCHAR2(50) NOT NULL,
    CONSTRAINT PK_REQUISICAO_ENTREGA PRIMARY KEY (REQUISICAO_),
    CONSTRAINT FK_REQ_REQ_ENTREGA FOREIGN KEY (REQUISICAO_) REFERENCES REQUISICAO (ID)
);

CREATE TABLE ENTREGA (
    CODIGO NUMBER NOT NULL,
    REQ_ENTREGA_ NUMBER NOT NULL,
    TRANSPORTADOR_ VARCHAR2(14) NOT NULL,
    VEICULO VARCHAR2(20) NOT NULL,
    STATUS VARCHAR2(10) NOT NULL,
    DATA_COLETA DATE NOT NULL,
    PREVISAO_ENTREGA DATE,
    DATA_ENTREGA DATE,
    CONSTRAINT PK_ENTREGA_CODIGO PRIMARY KEY (CODIGO),
    CONSTRAINT UN_ENTREGA_REQ UNIQUE (REQ_ENTREGA_),
    CONSTRAINT FK_ENTREGA_TRA FOREIGN KEY (TRANSPORTADOR_) REFERENCES TRANSPORTADOR (USUARIO_TRA),
    CONSTRAINT FK_REQ_ENTREGA FOREIGN KEY (REQ_ENTREGA_) REFERENCES REQ_ENTREGA (REQUISICAO_),
    CONSTRAINT CHECK_STATUS_ENTREGA_VAL CHECK((STATUS) IN ('PENDENTE', 'EM_ROTA', 'FINALIZADA')), --DEFINIR TIPOS
    CONSTRAINT CHECK_DATAS_ENTREGA CHECK (DATA_COLETA <= DATA_ENTREGA)

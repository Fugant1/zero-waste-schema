-- BLOCO BENEFÍCIARIO
-- NESSE BLOCO OBSERVAM-SE TABELAS RELACIONADAS AOS USUÁRIOS BENEFICIÁRIOS
CREATE TABLE BENEFICIARIO (
    USUARIO_BEN VARCHAR2(14) NOT NULL,
    CONSTRAINT PK_USUARIO_BENEFICIARIO PRIMARY KEY (USUARIO_BEN), 
    CONSTRAINT FK_USUARIO_BEN_TIPO FOREIGN KEY (USUARIO_BEN) REFERENCES USUARIO (CPF_CNPJ) ON DELETE CASCADE
);

CREATE TABLE REQUISICAO (
    ID NUMBER NOT NULL,
    BENEFICIARIO_ VARCHAR2(14) NOT NULL,
    STATUS VARCHAR2(10),
    DATA_CRIACAO DATE,
    TIPO  VARCHAR2(8) DEFAULT 'RETIRADA' NOT NULL, --CASO O BENEFICIARIO N SE PRONUNCIE, RETIRADA
    CONSTRAINT PK_REQUISICAO PRIMARY KEY (ID),
    CONSTRAINT FK_BEN_REQ FOREIGN KEY (BENEFICIARIO_) REFERENCES BENEFICIARIO (USUARIO_BEN),
    CONSTRAINT CHECK_STATUS_REQUISICAO_VAL CHECK((STATUS) IN ('PENDENTE', 'FINALIZADA')), -- DEFINIR STATUS
    CONSTRAINT CHECK_TIPO_REQ CHECK((TIPO) IN ('RETIRADA', 'ENTREGA')) --DEFINIR COMO TRIGGER ESCUDOS PARA N CRIAR DATA MENOR Q O MOMENTO ATUAL
);

CREATE TABLE REQ_RETIRADA (
    REQUISICAO_ NUMBER NOT NULL,
    DATA_RETIRADA DATE,
    CONSTRAINT PK_REQ_RETIRADA PRIMARY KEY (REQUISICAO_),
    CONSTRAINT FK_REQ_RETIRADA FOREIGN KEY (REQUISICAO_) REFERENCES REQUISICAO (ID)
);

CREATE TABLE AVALIACAO (
    REQUISICAO_ NUMBER NOT NULL,
    DESCRICAO VARCHAR2(256),
    EH_DENUNCIA CHAR(1) DEFAULT '0' NOT NULL,
    EH_FEEDBACK CHAR(1) DEFAULT '0' NOT NULL,
    CONSTRAINT PK_AVALICAO_REQ PRIMARY KEY (REQUISICAO_),
    CONSTRAINT FK_AVALICAO_REQ FOREIGN KEY (REQUISICAO_) REFERENCES REQUISICAO (ID) ON DELETE CASCADE,
    CONSTRAINT CHECK_DEN_AVA CHECK (EH_DENUNCIA IN ('0','1')),
    CONSTRAINT CHECK_FED_AVA CHECK (EH_FEEDBACK IN ('0','1')), 
    CONSTRAINT CHECK_DEN_DIF_FED_AVA CHECK (NOT (EH_DENUNCIA = '0' AND EH_FEEDBACK = '0')) 
);


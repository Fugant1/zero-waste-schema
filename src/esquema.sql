-- ADMIN PARA MODERAÇÃO DE DENÚNCIAS
CREATE TABLE ADMIN (
    USUARIO_ADM VARCHAR2(14) NOT NULL,
    CONSTRAINT PK_USUARIO_ADMIN PRIMARY KEY (USUARIO_ADM), 
    CONSTRAINT FK_USUARIO_ADM FOREIGN KEY (USUARIO_ADM) REFERENCES USUARIO (CPF_CNPJ) ON DELETE CASCADE
);

CREATE TABLE DENUNCIA (
    AVALIACAO_REQ NUMBER NOT NULL,
    CATEGORIA VARCHAR2(25) NOT NULL,
    CONSTRAINT PK_DENUNCIA_REQ PRIMARY KEY (AVALIACAO_REQ),
    CONSTRAINT FK_DENUNCIA_REQ FOREIGN KEY (AVALIACAO_REQ) REFERENCES AVALIACAO (REQUISICAO_) ON DELETE CASCADE,
    CONSTRAINT CHECK_CAT_DENUN CHECK(CATEGORIA IN (
        'PRODUTO_VENCIDO', 
        'DIVERGENCIA', 
        'FALHA_LOGISTICA', 
        'EXTRAVIO', 
        'MAU_ACONDICIONAMENTO', 
        'FRAUDE', 
        'COMPORTAMENTO', 
        'OUTROS'
    ))
);

CREATE TABLE FEEDBACK (
    AVALIACAO_REQ NUMBER NOT NULL,
    NOTA NUMBER NOT NULL,
    CONSTRAINT PK_FEEDBACK_REQ PRIMARY KEY (AVALIACAO_REQ),
    CONSTRAINT FK_FEEDBACK_REQ FOREIGN KEY (AVALIACAO_REQ) REFERENCES AVALIACAO (REQUISICAO_) ON DELETE CASCADE,
    CONSTRAINT CHECK_NOTA_VAL CHECK((NOTA) > 0 AND (NOTA) <= 5)
);

CREATE TABLE MODERACAO (
    ADM VARCHAR2(14) NOT NULL,
    DOADOR VARCHAR2(14) NOT NULL,
    CONSTRAINT PK_MODERACAO PRIMARY KEY (ADM, DOADOR),
    CONSTRAINT FK_USUARIO_ADM_MODERA FOREIGN KEY (ADM) REFERENCES ADMIN (USUARIO_ADM),
    CONSTRAINT FK_USUARIO_DOADOR_MODERA FOREIGN KEY (DOADOR) REFERENCES DOADOR (USUARIO_DOA)
);


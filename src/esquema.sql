--DEFINIÇÃO DOS TRIGGERS PARA DATAS INVÁLIDAS

CREATE OR REPLACE TRIGGER TRG_VALIDA_REQUISICAO
BEFORE INSERT OR UPDATE ON REQUISICAO
FOR EACH ROW
BEGIN
    IF :NEW.DATA_CRIACAO < SYSDATE THEN
        RAISE_APPLICATION_ERROR(-20001, 'A data de criação da requisição deve ser superior ou igual a data atual.');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_VALIDA_LOTE
BEFORE INSERT OR UPDATE ON LOTE_ITEM
FOR EACH ROW
BEGIN
    IF :NEW.VALIDADE <= SYSDATE THEN
        RAISE_APPLICATION_ERROR(-20002, 'A validade do item deve ser superior a data atual.');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_VALIDA_REQ_RETIRADA
BEFORE INSERT OR UPDATE ON REQ_RETIRADA
FOR EACH ROW
BEGIN
    IF :NEW.DATA_RETIRADA < SYSDATE THEN
        RAISE_APPLICATION_ERROR(-20003, 'A data de retirada do item deve ser superior ou igual a data atual.');
    END IF;
END;
/

EXIT;